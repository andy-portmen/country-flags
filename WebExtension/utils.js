utils.aton6 = a => {
  a = a.replace(/"/g, '').split(/:/);

  var l = a.length - 1;
  var i;

  if (a[l] === '') {
    a[l] = 0;
  }

  if (l < 7) {
    a.length = 8;

    for (i = l; i >= 0 && a[i] !== ''; i--) {
      a[7 - l + i] = a[i];
    }
  }

  for (i = 0; i < 8; i++) {
    if (!a[i]) {
      a[i] = 0;
    }
    else {
      a[i] = parseInt(a[i], 16);
    }
  }

  var r = [];
  for (i = 0; i < 4; i++) {
    r.push(((a[2 * i] << 16) + a[2 * i + 1]) >>> 0);
  }

  return r;
};

utils.cmp6 = (a, b) => {
  for (var ii = 0; ii < 2; ii++) {
    if (a[ii] < b[ii]) {
      return -1;
    }

    if (a[ii] > b[ii]) {
      return 1;
    }
  }

  return 0;
};
